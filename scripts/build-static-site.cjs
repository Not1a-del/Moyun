const fs = require('node:fs');
const path = require('node:path');

const root = path.resolve(__dirname, '..');
const sourcePath = path.join(root, 'source', 'moyun.single.html');
const outputHtmlPath = path.join(root, 'index.html');
const cssPath = path.join(root, 'assets', 'css', 'moyun.css');
const jsPath = path.join(root, 'assets', 'js', 'moyun.js');

function assertSingle(match, label) {
  if (match.length !== 1) throw new Error(`Expected one ${label}, found ${match.length}`);
}

const source = fs.readFileSync(sourcePath, 'utf8');
const styles = source.match(/<style>[\s\S]*?<\/style>/g) || [];
const inlineScripts = source.match(/<script(?![^>]*\bsrc=)[^>]*>[\s\S]*?<\/script>/g) || [];

assertSingle(styles, 'inline style block');
assertSingle(inlineScripts, 'inline application script');

const css = styles[0].replace(/^<style>|<\/style>$/g, '');
const js = inlineScripts[0].replace(/^<script[^>]*>|<\/script>$/g, '');
let html = source.replace(styles[0], '<link rel="stylesheet" href="./assets/css/moyun.css">');
html = html.replace(inlineScripts[0], '<script src="./assets/js/moyun.js"></script>');

fs.mkdirSync(path.dirname(cssPath), { recursive: true });
fs.mkdirSync(path.dirname(jsPath), { recursive: true });
fs.writeFileSync(cssPath, '/* Generated from source/moyun.single.html. Do not edit directly. */\n' + css);
fs.writeFileSync(jsPath, '/* Generated from source/moyun.single.html. Do not edit directly. */\n' + js);
fs.writeFileSync(outputHtmlPath, '<!-- Generated by scripts/build-static-site.cjs. -->\n' + html);

console.log(JSON.stringify({ cssPath, jsPath, outputHtmlPath }));
